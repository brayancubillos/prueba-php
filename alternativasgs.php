        <b>¿Qué es Contrato de Aprendizaje?</b><br>
        - No es contrato laboral.<br>
        - No genera relación laboral.<br>
        - Es una forma de vinculo especial dentro del derecho laboral, donde media la formación.<br>
        - La subordinación se presenta solo en las actividades propias del aprendizaje y por un plazo no mayor a dos (2) años ajustado al programa de formación.<br>
        - Es un contrato formativo con exclusividad en las actividades propias del aprendizaje.<br>
        - El contrato de aprendizaje puede ser celebrado por personas mayores de 14 años.<br>
        - Se puede celebrar sin ninguna clase de permiso especial.<br>
        - Se genera un reconocimiento de un apoyo de sostenimiento que le garantice al aprendiz el proceso de formación en las etapas lectiva y productiva, el cual en ningún caso constituye salario o sueldo.<br>
        <br>
        <b>Etapa lectiva:</b><br>
        - Afiliación a salud.<br>
        - Apoyo de sostenimiento 50% S.M.M.L.V.<br>
        <br>
        <b>Etapa productiva:</b><br>
        - Afiliación a salud.<br>
        - Riesgos laborales.<br>
        - Apoyo de sostenimiento.<br>
        <br>
        <b>Obligaciones:</b><br>
        - No presentarse a la empresa a realizar prácticas.<br>
        - Adelantar la formación integral de cada programa de formación.<br>
        - Asistir a la formación.<br>
        - Mantener un buen nivel académico.<br>
        - Profundizar sobre herramientas ofimáticas: Manejo de Excel - Word - PowerPoint.<br>
        - Portar el uniforme. Buena presentación personal.<br>
        - Cumplir el horario de la empresa. 8 horas diarias, hasta 48 horas semanales.<br>
        - Ser respetuoso, cordial y responsable.<br>
        <br>
        <b>Causales de suspensión del contraro de aprendizaje (Acuerdo 15 de 2003):</b><br>
        - Licencia de maternidad.<br>
        - Incapacidades debidamente certificadas.<br>
        - Vacaciones del empleador.<br>
        - Caso fortuito o fuerza mayor.<br>
        - Mutuo acuerdo entre las partes (SENA - APRENDIZ - EMPRESA).<br>
        - Vencimiento del término de duración del contrato.<br>
        - Bajo rendimiento o faltas disciplinarias en la etapa lectiva o en la etapa produtiva.<br>
        - Cancelación del registro de matrícula por parte del SENA.<br>
        - Incumplimiento de las obligaciones previstas por cada una de las partes.<br>
        <br>
        <b>Se considera falta GRAVE:</b><br>
        - La terminación unilateral del Contrato de Aprendizaje por parte del aprendiz. (Capitulo IV Artículo 10 Numeral 2).<br>
        - No asistir a las entrevistas para la ejecución del contrato de aprendizaje. (Capítulo IV Artículo 10 Numeral 26).<br>
        - No asistir a las entrevistas y firmar contratos de aprendizaje sin realizar la gestión a través del aplicativo SGVA, o sin encontrarse registrado como aprendiz disponible en dicha plataforma. (Capítulo IV Artículo 10 Numeral 26).<br>
        - Presentarse con cartas o documentos diferentes a los expedidos por la Oficina de Relaciones Corporativas para auto gestionar un contrato de aprendizaje. (Capítulo IV Artículo 10 Numeral 29).<br>

